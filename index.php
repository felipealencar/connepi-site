                O alcance do CONNEPI vai além dos limites do ensino e do fazer científico, pois os resultados de grande parte das pesquisas apresentadas no evento interferem diretamente no setor produtivo do Brasil, bem como na vida do cidadão/ã brasileiro/a.
                Este ano a XI edição do Congresso Norte-Nordeste de Pesquisa e Inovação (CONNEPI) reflete um momento muito especial para a afirmação das políticas de indução e de desenvolvimento científico e tecnológico da rede de educação profissional e tecnológica. Um ano de incertezas políticas e econômicas que interferem, sobremaneira,  na constituição de agendas sólidas e contínuas para o avanço científico nacional, refletidas na formação de pesquisadores, de interfaces entre a academia, governo e os setores produtivos e sociais, que justificam ainda mais a necessidade de consolidação deste evento, dado o seu caráter indutor e promotor de desenvolvimento técnico-científico e que se relacione diretamente com os fazeres produtivos de toda a sociedade.
                Assim, o CONNEPI-IFAL-2016 que, este ano, traz como o tema Políticas de inovação como estratégias de fortalecimento para a rede, traduz uma ideia da vontade de continuidade de um projeto que nasceu há mais de uma década e que vem se consolidando como a mais importante iniciativa para um conjunto de 18 instituições do Norte e do Nordeste que precisam ver articulados os esforços para a promoção da ciência e tecnologia de seus 270 campi.
                Espera-se ver em Maceió, a exemplo de outras edições do CONNEPI, mais de 2.000 (duas mil) pessoas comungando de suas diversas atividades.
